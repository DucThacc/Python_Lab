""" Accessing List Items:
 • You can access elements in a list by referring to their index number inside 
square brackets [].
 • This is similar to accessing characters in a string.
 
 listname[index]
 listname[index1:index2]
 listname[:index]
 listname[index:]
 """

list1=["apple", "banana", "cherry"]
print(list1[0])        
print(list1[-1])       
print(list1[0:2])
print(list1[:2])      
print(list1[1:])  

